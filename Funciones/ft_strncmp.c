#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <ctype.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	if (n == 0)
		return (s1[i] - s2[i]);
	while (s1[i] == s2[i] && i < (n - 1))
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}

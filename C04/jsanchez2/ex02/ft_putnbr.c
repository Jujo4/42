#include <unistd.h>

void	ft_putnbr(int nb)
{
	char nbr;

	if (nb > -2147483648 && nb <= 2147483647)
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			ft_putnbr(nb * (-1));
		}
		else if (nb >= 10)
		{
			ft_putnbr(nb / 10);
			nbr = (nb % 10) + 48;
			write(1, &nbr, 1);
		}
		else
		{
			nbr = nb + 48;
			write(1, &nbr, 1);
		}
	}
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
}
